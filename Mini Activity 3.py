price = float(input("What is the price of the item?"))
tax_rate = float(input("What is the current tax rate in decimal form?"))

# Information Gathering

tax = price * tax_rate

total = tax + price

# Calculating Amounts

print(f"Price: ${price:.2f}")
print(f"Tax: ${tax:.2f}")
print(f"Total: ${total:.2f}")




      

                

    
